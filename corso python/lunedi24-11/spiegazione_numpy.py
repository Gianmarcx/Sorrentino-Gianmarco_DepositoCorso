#Array N-dimensionali: NumPy introduce l'oggetto ndarray, una struttura di dati N-dimensionale, che è una generalizzazione degli array unidimensionali e bidimensionali (matrici).

#Operazioni Vettoriali: Le operazioni aritmetiche su array di NumPy sono eseguite in modo vettoriale, il che significa che operazioni come somma, sottrazione, moltiplicazione e divisione possono essere applicate a interi array senza la necessità di loop espliciti.

#Funzioni Universali (ufuncs): NumPy fornisce funzioni universali che eseguono operazioni element-wise su array. Esempi di ufuncs includono funzioni matematiche come sin, cos, exp, ecc.

